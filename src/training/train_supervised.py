def compute_masked_mse(pred_probs: torch.Tensor, targets: torch.Tensor, mask: torch.Tensor) -> torch.Tensor:
    pred_flat = pred_probs.squeeze(1)[mask]
    target_flat = targets[mask]
    if len(pred_flat) == 0:
        return torch.tensor(0.0, device=pred_probs.device, requires_grad=True)
    return F.mse_loss(pred_flat, target_flat)

def compute_action_accuracy(pred_probs: torch.Tensor, target_probs: torch.Tensor, mask: torch.Tensor) -> float:
    preds = pred_probs.squeeze(1).detach().cpu().numpy()
    targets = target_probs.detach().cpu().numpy()
    masks = mask.detach().cpu().numpy()
    B = preds.shape[0]
    correct = 0
    for i in range(B):
        p = preds[i]
        t = targets[i]
        m = masks[i]
        if not m.any():
            correct += 1
            continue
        p_masked = np.where(m, p, 2.0)
        best_idx = np.argmin(p_masked)
        r, c = divmod(best_idx, p.shape[1])
        if t[r, c] == 0.0:
            correct += 1
    return correct / max(1, B)

def train_epoch(
    model: MinesweeperTransformer,
    dataloader: DataLoader,
    optimizer: torch.optim.Optimizer,
    device: str | torch.device,
    grad_clip_norm: float = 1.0,
    refinement_steps: int = 4,
) -> float:
    model.train()
    total_loss = 0.0
    for batch_idx, (channels, probs, mask) in enumerate(dataloader):
        channels = channels.to(device)
        probs = probs.to(device)
        mask = mask.to(device)
        B = channels.shape[0]
        optimizer.zero_grad()
        
        mem_state = torch.zeros((B, model.config.hidden_channels, channels.shape[2], channels.shape[3]), device=device)
        prev_probs = torch.full((B, 1, channels.shape[2], channels.shape[3]), 0.5, device=device)

        for step in range(refinement_steps):
            prev_probs, mem_state = model._single_pass(channels, prev_probs, mem_state)

        loss = compute_masked_mse(prev_probs, probs, mask)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), grad_clip_norm)
        optimizer.step()
        total_loss += loss.item()
    return total_loss / len(dataloader)

def validate(
    model: MinesweeperTransformer,
    dataloader: DataLoader,
    device: str | torch.device,
    refinement_steps: int = 4,
) -> tuple[float, float, float]:
    model.eval()
    total_loss = 0.0
    total_correct = 0
    total_action_correct = 0
    total_cells = 0
    total_action_samples = 0
    with torch.no_grad():
        for channels, probs, mask in dataloader:
            channels = channels.to(device)
            probs = probs.to(device)
            mask = mask.to(device)

            probs_out, _ = model.forward(channels, num_refine_steps=refinement_steps)
            loss = compute_masked_mse(probs_out, probs, mask)
            total_loss += loss.item()

            preds = (probs_out.squeeze(1) > 0.5).float()
            targets = (probs > 0.5).float()
            total_correct += (preds[mask] == targets[mask]).sum().item()
            total_cells += mask.sum().item()

            act_acc = compute_action_accuracy(probs_out, probs, mask)
            total_action_correct += act_acc * mask.shape[0]
            total_action_samples += mask.shape[0]

    avg_loss = total_loss / len(dataloader)
    accuracy = total_correct / max(1, total_cells)
    action_accuracy = total_action_correct / max(1, total_action_samples)
    return avg_loss, accuracy, action_accuracy

def train_supervised(config: TrainingConfig) -> TrainingMetrics:
    device = torch.device(config.device)
    print(f"Training on: {device}")
    
    train_dataset = MinesweeperDataset(Path(config.data_dir), split="train", augment=True)
    val_dataset = MinesweeperDataset(Path(config.data_dir), split="val", augment=False)
    train_loader = DataLoader(train_dataset, batch_size=64, shuffle=True)
    val_loader = DataLoader(val_dataset, batch_size=64, shuffle=False)
    
    model = MinesweeperTransformer(ModelConfig()).to(device)
    optimizer = torch.optim.AdamW(model.parameters(), lr=config.learning_rate, weight_decay=config.weight_decay)
    
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=config.epochs)
    
    metrics = TrainingMetrics()
    best_val_loss = float('inf')
    save_dir = Path(config.save_dir)
    save_dir.mkdir(parents=True, exist_ok=True)
    
    for epoch in range(config.epochs):
        t0 = time.time()
        train_loss = train_epoch(model, train_loader, optimizer, device, config.grad_clip_norm, config.refinement_steps)
        val_loss, val_acc, val_act_acc = validate(model, val_loader, device, config.refinement_steps)
        scheduler.step()
        
        metrics.train_loss.append(train_loss)
        metrics.val_action_accuracy.append(val_act_acc)
        
        print(f"Epoch {epoch+1}/{config.epochs} [{time.time()-t0:.1f}s] "
              f"Train Loss: {train_loss:.4f} | Val Loss: {val_loss:.4f} | "
              f"Val Acc: {val_acc:.1%} | Act Acc: {val_act_acc:.1%}")
              
        if val_loss < best_val_loss:
            best_val_loss = val_loss
            metrics.best_epoch = epoch + 1
            metrics.best_win_rate = val_act_acc
            torch.save({
                "model_state_dict": model.state_dict(),
                "optimizer_state_dict": optimizer.state_dict(),
                "epoch": epoch + 1,
                "best_val_loss": best_val_loss,
            }, save_dir / "best_model.pt")
            print("  -> Saved new best model")
            
    return metrics
