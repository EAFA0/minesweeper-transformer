"""Supervised MSE Training (Offline npz).

Loads .npz files generated by the data pipeline, provides
(channels, probs, masks) batches for supervised training.
"""
import os
import shutil
import time
from datetime import datetime
from dataclasses import dataclass, field
from pathlib import Path
from typing import List, Optional

import numpy as np
import torch
import torch.nn.functional as F
import torch.optim as optim
from tqdm import tqdm

from config import POLICY, TrainingConfig, TrainingMetrics, ModelConfig
from model.architecture import MinesweeperTransformer
from training.trajectory_pool import TrajectoryPool


def train_supervised(
    config: TrainingConfig,
    model_cfg: ModelConfig,
    device: Optional[str] = None,
    arch: str = "V4",
    run_dir: str = "",
):
    """Train the model offline using TrajectoryPool's batch()."""
    if not config.data_dir:
        raise ValueError(
            "Offline SUPERVISED mode strictly requires a valid 'data_dir' to load .npz files. "
            "Please specify it via CLI (e.g. --data_dir data) or ensure your config provides a default."
        )

    from utils.device import get_device
    device = get_device(device if device is not None else config.device)
        
    run_dir = run_dir or datetime.now().strftime("runs/%Y%m%d_%H%M%S")
    os.makedirs(run_dir, exist_ok=True)
    
    # Initialize the unified TrajectoryPool for offline supervised mode
    # compute_probs=True so we have targets to distill
    pool = TrajectoryPool(
        board_width=config.board_width,
        board_height=config.board_height,
        board_mines=config.board_mines,
        pool_size=config.pool_size,
        pool_workers=config.pool_workers,
        data_dir=config.data_dir,
        mixed_mode=config.mixed_mode,
        compute_probs=True,
    )
    
    if arch == "V1":
        from model.architecture_v1 import MinesweeperTransformerV1
        model = MinesweeperTransformerV1(model_cfg).to(device)
    elif arch == "V1_5":
        from model.architecture_v1_5 import MinesweeperTransformerV1_5
        model = MinesweeperTransformerV1_5(model_cfg).to(device)
    else:
        from model.architecture import MinesweeperTransformer
        model = MinesweeperTransformer(model_cfg).to(device)
        
    if config.pretrained:
        print(f"Loading pretrained: {config.pretrained}")
        model.load_pretrained(config.pretrained, device=device)
        
    optimizer = optim.AdamW(model.parameters(), lr=config.learning_rate)
    
    print(f"Training on {device} - Offline SUPERVISED mode - Arch: {arch} - Loss: {config.loss_type}")
    print(f"Board Config: {config.board_width}x{config.board_height} with {config.board_mines} mines")
    print(f"Run dir: {run_dir}")

    # ── Loss setup ──────────────────────────────────────────────────────────
    target_type = "binary" if config.loss_type == "bce" else "probs"
    if config.loss_type == "bce":
        total_cells = config.board_width * config.board_height
        mines = config.board_mines
        pos_weight_val = (total_cells - mines) / max(mines, 1)
        print(f"BCE Loss with binary (ground-truth) targets, pos_weight={pos_weight_val:.2f}")
    else:
        pos_weight_val = None
        print(f"MSE Loss with solver probability targets (distillation)")
    
    # Calculate batches per epoch correctly based on states, not just games
    batch_size = getattr(config, 'batch_size', 64)
    if pool.total_states > 0 and config.data_dir == "data":
        # Fallback if dynamic gen but some states preloaded from default dir
        estimated_states = config.n_games * 15
        batches_per_epoch = max(1, estimated_states // batch_size)
        print(f"Dataset: Generating on the fly. Estimated {estimated_states} states -> {batches_per_epoch} batches/epoch")
    elif pool.total_states > 0:
        # 1 epoch = seeing all states in the offline dataset once on average
        batches_per_epoch = max(1, pool.total_states // batch_size)
        print(f"Dataset: {pool.total_games} games, {pool.total_states} states -> {batches_per_epoch} batches/epoch")
    else:
        # Fallback if generating on the fly (assume ~15 states per game)
        estimated_states = config.n_games * 15
        batches_per_epoch = max(1, estimated_states // batch_size)
        print(f"Dataset: Generating on the fly. Estimated {estimated_states} states -> {batches_per_epoch} batches/epoch")
        
    scheduler = optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=batches_per_epoch * config.epochs, eta_min=config.min_lr
    )
    
    best_wr = 0.0
    
    for epoch in range(1, config.epochs + 1):
        model.train()
        total_loss = 0.0
        
        pbar = tqdm(total=batches_per_epoch, desc=f"Epoch {epoch}/{config.epochs}")
        
        for _ in range(batches_per_epoch):
            # 1. Fetch batch from unified pool
            channels, targets, masks = pool.batch(batch_size, target_type=target_type)

            channels = channels.to(device)
            targets = targets.to(device)
            masks = masks.to(device)

            # 2. Forward pass
            if arch == "V1":
                # V1: single forward pass, no refinement
                preds = model(channels)
                if config.loss_type == "bce":
                    logits = preds[:, 0]  # (B, H, W) raw logits
                    pos_weight = torch.tensor(pos_weight_val, device=device)
                    loss = F.binary_cross_entropy_with_logits(
                        logits[masks], targets[masks],
                        pos_weight=pos_weight
                    )
                else:
                    probs = torch.sigmoid(preds[:, 0])  # (B, H, W)
                    loss = F.mse_loss(probs[masks], targets[masks])

            elif arch == "V1_5":
                # V1_5: iterative refinement with full BPTT
                refine_results = model.refine(
                    channels, num_steps=config.refinement_steps,
                    return_logits=True
                )
                raw = refine_results[-1]  # (B, 2, H, W) — final step raw logits
                if config.loss_type == "bce":
                    logits = raw[:, 0]  # (B, H, W) raw logits
                    pos_weight = torch.tensor(pos_weight_val, device=device)
                    loss = F.binary_cross_entropy_with_logits(
                        logits[masks], targets[masks],
                        pos_weight=pos_weight
                    )
                else:
                    probs = torch.sigmoid(raw[:, 0])  # (B, H, W)
                    loss = F.mse_loss(probs[masks], targets[masks])

            elif arch == "V2":
                # V2: single forward pass (no refine)
                preds = model(channels)
                if config.loss_type == "bce":
                    logits = preds[:, 0]
                    pos_weight = torch.tensor(pos_weight_val, device=device)
                    loss = F.binary_cross_entropy_with_logits(
                        logits[masks], targets[masks],
                        pos_weight=pos_weight
                    )
                else:
                    probs = torch.sigmoid(preds[:, 0])
                    loss = F.mse_loss(probs[masks], targets[masks])

            else:
                # V4: iterative refinement with full BPTT (grounding + residual)
                refine_results = model.refine(channels, num_steps=config.refinement_steps)
                probs = refine_results[-1][:, 0]  # (B, H, W) — sigmoid'd mine probs
                if config.loss_type == "bce":
                    # V4 refine returns sigmoid'd → use BCE on probabilities
                    loss = F.binary_cross_entropy(probs[masks], targets[masks])
                else:
                    loss = F.mse_loss(probs[masks], targets[masks])
            
            # 4. Backward
            optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), config.grad_clip_norm)
            optimizer.step()
            scheduler.step()
            
            total_loss += loss.item()
            pbar.update(1)
            pbar.set_postfix({"loss": f"{loss.item():.4f}", "lr": f"{scheduler.get_last_lr()[0]:.1e}"})
            
        pbar.close()
        avg_loss = total_loss / batches_per_epoch
        print(f"Epoch {epoch} complete - Avg Loss: {avg_loss:.4f}")
        
        # Periodic Evaluation
        from training.evaluate import evaluate_model
        print(f"\n  ── Evaluating Epoch {epoch} ──")
        model.eval()
        eval_metrics = evaluate_model(
            model, device,
            n_games=config.eval_games,
            width=config.board_width,
            height=config.board_height,
            total_mines=config.board_mines,
            board_pool_path=config.board_pool_path,
            refine_steps=config.refinement_steps,
            quiet=False
        )
        wr = eval_metrics["win_rate"]
        
        # Save checkpoint with full metadata
        ckpt_path = os.path.join(run_dir, f"model_ep{epoch}.pt")
        torch.save({
            "epoch": epoch,
            "arch_version": arch,
            "model_state_dict": model.state_dict(),
            "optimizer_state_dict": optimizer.state_dict(),
            "scheduler_state_dict": scheduler.state_dict(),
            "model_config": model_cfg,
            "win_rate": wr,
        }, ckpt_path)
        print(f"Saved checkpoint to {ckpt_path}")
        
        if wr > best_wr:
            best_wr = wr
            shutil.copy2(ckpt_path, os.path.join(run_dir, "best_model.pt"))
            print(f"  🏆 New best model! Win rate: {best_wr:.1%}")
