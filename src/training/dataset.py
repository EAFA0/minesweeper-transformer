"""Dataset and DataLoader for Minesweeper training data (probability distillation).

Loads .npz files generated by the data pipeline, provides
(channels, probs, masks) batches for supervised training.

Labels are probability matrices (P(mine) per cell) from the ProbabilitySolver.
Includes D4 data augmentation: random rotation/flip.
"""

from pathlib import Path
from typing import List, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset


# ─── D4 Augmentation (8 symmetries of a square) ────────────────────────────

def _apply_d4(channels: torch.Tensor, probs: torch.Tensor, mask: torch.Tensor,
              op_idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
    """Apply one of 8 D4 symmetry operations.

    D4 = {rot0°, rot90°, rot180°, rot270°} × {no flip, horizontal flip}
    op_idx: 0-7 mapping as:
      0-3: rot k×90°, no flip
      4-7: rot k×90°, horizontal flip
    """
    k = op_idx % 4          # number of 90° rotations
    flip = op_idx >= 4      # whether to flip horizontally

    if flip:
        channels = torch.flip(channels, dims=[2])   # flip width dim
        probs = torch.flip(probs, dims=[1])
        mask = torch.flip(mask, dims=[1])

    if k > 0:
        channels = torch.rot90(channels, k=k, dims=[1, 2])
        probs = torch.rot90(probs, k=k, dims=[0, 1])
        mask = torch.rot90(mask, k=k, dims=[0, 1])

    return channels, probs, mask


class MinesweeperDataset(Dataset):
    """PyTorch Dataset for probability-distilled minesweeper training samples.

    Each sample is a (channels, probs, mask) tuple from one game step.
    Channels: (10, H, W) float32 — covered/flagged/number one-hot
    Probs:    (H, W) float32 — solver-computed P(mine) per cell
    Mask:     (H, W) bool — 1 for cells contributing to loss (covered cells)

    Supports D4 data augmentation (random rotation/flip) for train split.
    """

    def __init__(
        self,
        data_dir: Path,
        split: str = "train",
        val_ratio: float = 0.1,
        seed: int = 42,
        augment: bool = True,
    ):
        self.data_dir = Path(data_dir)
        self.split = split
        self.augment = augment and split == "train"

        # Collect all .npz data files
        data_files = sorted(self.data_dir.glob("data_*.npz"))
        if not data_files:
            raise FileNotFoundError(f"No data_*.npz files found in {self.data_dir}")

        # Load all data into memory
        all_channels: List[np.ndarray] = []
        all_probs: List[np.ndarray] = []
        all_masks: List[np.ndarray] = []

        for f in data_files:
            data = np.load(f)
            all_channels.append(data["channels"])
            all_probs.append(data["probs"])
            all_masks.append(data["masks"])

        channels = np.concatenate(all_channels, axis=0)
        probs = np.concatenate(all_probs, axis=0)
        masks = np.concatenate(all_masks, axis=0)

        # Train/val split (deterministic by seed)
        n_total = len(channels)
        indices = np.arange(n_total)
        rng = np.random.default_rng(seed)
        rng.shuffle(indices)
        n_val = int(n_total * val_ratio)

        if split == "train":
            indices = indices[n_val:]
        elif split == "val":
            indices = indices[:n_val]
        else:
            raise ValueError(f"Unknown split: {split}")

        self.channels = channels[indices]
        self.probs = probs[indices]
        self.masks = masks[indices]

        # Statistics
        valid = self.masks == 1
        self.probs_masked = self.probs[valid]
        self.n_cells = len(self.probs_masked)

        # Bin distribution: fraction of probs in [0], (0,1), [1]
        self.frac_zero = float(np.mean(self.probs_masked == 0.0))
        self.frac_one = float(np.mean(self.probs_masked == 1.0))
        self.frac_ambiguous = float(np.mean((self.probs_masked > 0.0) & (self.probs_masked < 1.0)))

    def __len__(self) -> int:
        return len(self.channels)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        channels = torch.from_numpy(self.channels[idx]).float()
        probs = torch.from_numpy(self.probs[idx]).float()
        mask = torch.from_numpy(self.masks[idx]).bool()

        if self.augment:
            op = np.random.randint(0, 8)
            channels, probs, mask = _apply_d4(channels, probs, mask, op)

        return channels, probs, mask

    @property
    def prob_distribution(self) -> dict:
        """Summary of probability distribution in the dataset."""
        return {
            "n_cells": self.n_cells,
            "frac_deduced_safe": self.frac_zero,
            "frac_deduced_mine": self.frac_one,
            "frac_ambiguous": self.frac_ambiguous,
        }
