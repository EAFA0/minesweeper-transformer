"""Dataset and DataLoader for Minesweeper training data.

Loads .npz files generated by the data pipeline, provides
(channels, labels, masks) batches for supervised training.

Includes D4 (dihedral group) data augmentation: random rotation/flip
that preserves minesweeper game logic while increasing data diversity 8×.
"""

from pathlib import Path
from typing import List, Optional, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset


# ─── D4 Augmentation (8 symmetries of a square) ────────────────────────────

def _apply_d4(channels: torch.Tensor, labels: torch.Tensor, mask: torch.Tensor,
              op_idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
    """Apply one of 8 D4 symmetry operations.

    D4 = {rot0°, rot90°, rot180°, rot270°} × {no flip, horizontal flip}
    op_idx: 0-7 mapping as:
      0-3: rot k×90°, no flip
      4-7: rot k×90°, horizontal flip
    """
    k = op_idx % 4          # number of 90° rotations
    flip = op_idx >= 4      # whether to flip horizontally

    c, h, w = channels.shape

    if flip:
        channels = torch.flip(channels, dims=[2])   # flip width dim
        labels = torch.flip(labels, dims=[1])
        mask = torch.flip(mask, dims=[1])

    if k > 0:
        # rot90 with k times, rotating the last two dimensions (H, W)
        channels = torch.rot90(channels, k=k, dims=[1, 2])
        labels = torch.rot90(labels, k=k, dims=[0, 1])
        mask = torch.rot90(mask, k=k, dims=[0, 1])

    return channels, labels, mask


class MinesweeperDataset(Dataset):
    """PyTorch Dataset for pre-generated minesweeper training samples.

    Each sample is a (channels, labels, mask) tuple from one game step.
    Channels: (10, H, W) float32 — covered/flagged/number one-hot
    Labels:   (H, W) float32 — ground truth mine positions (0/1)
    Mask:     (H, W) bool — 1 for cells contributing to loss

    Supports D4 data augmentation (random rotation/flip) for train split.
    """

    def __init__(
        self,
        data_dir: Path,
        split: str = "train",
        val_ratio: float = 0.1,
        seed: int = 42,
        augment: bool = True,
    ):
        self.data_dir = Path(data_dir)
        self.split = split
        self.augment = augment and split == "train"

        # Collect all .npz data files
        data_files = sorted(self.data_dir.glob("data_*.npz"))
        if not data_files:
            raise FileNotFoundError(f"No data_*.npz files found in {self.data_dir}")

        # Load all data into memory
        all_channels: List[np.ndarray] = []
        all_labels: List[np.ndarray] = []
        all_masks: List[np.ndarray] = []

        for f in data_files:
            data = np.load(f)
            all_channels.append(data["channels"])
            all_labels.append(data["labels"])
            all_masks.append(data["masks"])

        channels = np.concatenate(all_channels, axis=0)
        labels = np.concatenate(all_labels, axis=0)
        masks = np.concatenate(all_masks, axis=0)

        # Train/val split (deterministic by seed)
        n_total = len(channels)
        indices = np.arange(n_total)
        rng = np.random.default_rng(seed)
        rng.shuffle(indices)
        n_val = int(n_total * val_ratio)

        if split == "train":
            indices = indices[n_val:]
        elif split == "val":
            indices = indices[:n_val]
        else:
            raise ValueError(f"Unknown split: {split}")

        self.channels = channels[indices]
        self.labels = labels[indices]
        self.masks = masks[indices]

        # Statistics
        valid = self.masks == 1
        self.n_mines = int((self.labels[valid] == 1).sum())
        self.n_safe = int((self.labels[valid] == 0).sum())

    def __len__(self) -> int:
        return len(self.channels)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        channels = torch.from_numpy(self.channels[idx]).float()
        labels = torch.from_numpy(self.labels[idx]).float()
        mask = torch.from_numpy(self.masks[idx]).bool()

        if self.augment:
            op = np.random.randint(0, 8)
            channels, labels, mask = _apply_d4(channels, labels, mask, op)

        return channels, labels, mask

    @property
    def mine_ratio(self) -> float:
        """Fraction of mines among masked cells."""
        return self.n_mines / max(1, self.n_mines + self.n_safe)
